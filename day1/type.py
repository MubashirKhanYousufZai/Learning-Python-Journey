# Typecasting = the process to convert one data type to another 
# str(), int(), floot(), bool()

full_name = "Mubashir Khan Yousufzai" 
age = 17 
percentile = 57.9 
is_student = True

full_name = str(full_name)
age = float(age)
percentile = int(percentile)
is_student = bool(is_student)
print(full_name)
print(age)
print(is_student)
print(percentile)
print(type(full_name))

